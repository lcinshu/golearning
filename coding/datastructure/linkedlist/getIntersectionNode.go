func getIntersectionNode(headA, headB *ListNode) *ListNode {
	if headA == nil || headB == nil {
		return nil
	}
	l1 := headA
	l2 := headB
	for l1 != l2 {
		if l1 != nil {
			l1 = l1.Next
		} else {
			l1 = headB
		}

		if l2 != nil {
			l2 = l2.Next
		} else {
			l2 = headA
		}
	}
	return l1
}

func buildListNode(x []int) *ListNode {
	l1 := &ListNode{x[0], nil}
	head := l1
	for i := 1; i < len(x); i++ {
		head.Next = &ListNode{x[i], nil}
		head = head.Next
	}
	return l1
}

